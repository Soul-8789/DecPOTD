class Solution {
  public:
    long long maxDiamonds(int A[], int N, int K) {
        // code here
        priority_queue<int> pq(A,A+N);
        long long sum=0;
        while(K--)
        {
           int tip=pq.top();
           pq.pop();
           pq.push(tip/2);
           sum+=tip;
        }
        return sum;
    }
};

